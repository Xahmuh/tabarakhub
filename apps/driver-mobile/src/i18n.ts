const isObject = (value: unknown): value is Record<string, unknown> =>
  !!value && typeof value === 'object' && !Array.isArray(value);

const mergeCopy = <T extends Record<string, unknown>>(base: T, override?: DeepPartial<T>): T => {
  const output: Record<string, unknown> = { ...base };
  Object.entries(override || {}).forEach(([key, value]) => {
    if (isObject(base[key]) && isObject(value)) {
      output[key] = mergeCopy(base[key] as Record<string, unknown>, value as Record<string, unknown>);
    } else if (value !== undefined) {
      output[key] = value;
    }
  });
  return output as T;
};

export const getDriverCopy = (language: DriverLanguage) =>
  mergeCopy(en, translations[language] || translations.en);

export const isRtlLanguage = (language: DriverLanguage) =>
  driverLanguageOptions.find(option => option.code === language)?.direction === 'rtl';

export const normalizeDriverLanguage = (value?: string | null): DriverLanguage =>
  driverLanguageOptions.some(option => option.code === value)
    ? (value as DriverLanguage)
    : 'en';

export const loadSavedDriverLanguage = async (): Promise<DriverLanguage> => {
  const stored = await AsyncStorage.getItem(DRIVER_LANGUAGE_STORAGE_KEY);
  return normalizeDriverLanguage(stored);
};

export const saveDriverLanguage = async (language: DriverLanguage) => {
  await AsyncStorage.setItem(DRIVER_LANGUAGE_STORAGE_KEY, language);
};

export const formatCopy = (template: string, values: Record<string, string | number>) =>
  Object.entries(values).reduce(
    (text, [key, value]) => text.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    template
  );
